"use client";

import type { SocialMedia } from "@/lib/types";

export function SocialLinks({ social, providerId }: { social: SocialMedia; providerId: number }) {
  const links = [
    { key: "facebook", url: social.facebook, label: "Facebook" },
    { key: "linkedin", url: social.linkedin, label: "LinkedIn" },
    { key: "instagram", url: social.instagram, label: "Instagram" },
    { key: "pinterest", url: social.pinterest, label: "Pinterest" },
  ].filter((l) => l.url);

  if (links.length === 0) return null;

  return (
    <div className="provider-hero-social">
      {links.map((link) => (
        <SocialLinkTracked
          key={link.key}
          href={link.url!}
          label={link.label}
          clickType={link.key}
          providerId={providerId}
        >
          <SocialIcon type={link.key} />
        </SocialLinkTracked>
      ))}
    </div>
  );
}

function SocialLinkTracked({
  href,
  label,
  clickType,
  providerId,
  children,
}: {
  href: string;
  label: string;
  clickType: string;
  providerId: number;
  children: React.ReactNode;
}) {
  const handleClick = () => {
    import("@/lib/api").then(({ trackingApi }) => {
      trackingApi.click({
        location: "provider_detail",
        click_type: clickType,
        provider_id: providerId,
      }).catch(() => {});
    });
  };

  return (
    <a
      href={href}
      target="_blank"
      rel="noopener noreferrer"
      className="provider-hero-social-link"
      aria-label={label}
      onClick={handleClick}
    >
      {children}
    </a>
  );
}

